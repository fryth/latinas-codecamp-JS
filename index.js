//SUMA

//Declaración de Función
function sum (value1, value2) {
    console.log(value1 + value2);
}
sum(160, 20);

//Expresión de Función
let sumResult = function sum(value1, value2) {
    console.log(value1 + value2);
}
sumResult(499, 49);

//Función Flecha
let sumResult2 = (value1, value2) => {
    console.log(value1 + value2);
}
sumResult2(891, 88);

